#!/usr/bin/env node
// Writes src/core/buildInfo.ts with the current git commit hash and app version.
// Used by `bun run build` so the embed is up-to-date at install time.

import { execSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outFile = resolve(__dirname, "..", "src", "core", "buildInfo.ts");

let commit = "unknown";
try {
  commit = execSync("git rev-parse HEAD", { encoding: "utf-8", timeout: 5000 }).trim();
} catch {
  // Not a git repo or git unavailable — keep default.
}

const pkg = JSON.parse(readFileSync(resolve(__dirname, "..", "package.json"), "utf-8"));
const appVersion = pkg.version;

const content = [
  "// Generated by scripts/gen-build-info.mjs - do not edit manually.",
  `export const BUILD_COMMIT = ${JSON.stringify(commit)} as const;`,
  `export const APP_VERSION = ${JSON.stringify(appVersion)} as const;`,
  "",
  `export const APP_NAME = "Codexa";`,
  `export const CODEXA_CHANNEL_ENV = "CODEXA_CHANNEL" as const;`,
  `export const LOCAL_DEV_CHANNEL = "local-dev" as const;`,
  `export type BuildChannel = "published" | typeof LOCAL_DEV_CHANNEL | string;`,
  "",
  "export interface AppBuildInfo {",
  "  appName: string;",
  "  version: string;",
  "  channel: BuildChannel;",
  "  isDev: boolean;",
  "  commit: string | null;",
  "  displayVersion: string;",
  "  brandLabel: string;",
  "}",
  "",
  "export function getBuildChannel(env: NodeJS.ProcessEnv = process.env): BuildChannel {",
  "  return env[CODEXA_CHANNEL_ENV]?.trim() || \"published\";",
  "}",
  "",
  "export function isDevBuild(env: NodeJS.ProcessEnv = process.env): boolean {",
  "  return getBuildChannel(env) === LOCAL_DEV_CHANNEL;",
  "}",
  "",
  "export function formatDisplayVersion(",
  "  version: string = APP_VERSION,",
  "  env: NodeJS.ProcessEnv = process.env,",
  "): string {",
  "  return isDevBuild(env) ? `${version}-dev` : version;",
  "}",
  "",
  "export function formatBrandLabel(env: NodeJS.ProcessEnv = process.env): string {",
  "  return `${APP_NAME} v${formatDisplayVersion(APP_VERSION, env)}`;",
  "}",
  "",
  "export function getAppBuildInfo(env: NodeJS.ProcessEnv = process.env): AppBuildInfo {",
  "  const channel = getBuildChannel(env);",
  "  const displayVersion = formatDisplayVersion(APP_VERSION, env);",
  "  return {",
  "    appName: APP_NAME,",
  "    version: APP_VERSION,",
  "    channel,",
  "    isDev: channel === LOCAL_DEV_CHANNEL,",
  "    commit: String(BUILD_COMMIT) && String(BUILD_COMMIT) !== \"unknown\" ? String(BUILD_COMMIT) : null,",
  "    displayVersion,",
  "    brandLabel: `${APP_NAME} v${displayVersion}`,",
  "  };",
  "}",
  "",
].join("\n");

writeFileSync(outFile, content, "utf-8");
console.log(`[gen-build-info] BUILD_COMMIT = ${commit.slice(0, 8)}, APP_VERSION = ${appVersion}`);
