#!/usr/bin/env node
// Writes src/config/buildInfo.ts with the current git commit hash.
// Used by `bun run build` so the embed is up-to-date at install time.

import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outFile = resolve(__dirname, "..", "src", "config", "buildInfo.ts");

let commit = "unknown";
try {
  commit = execSync("git rev-parse HEAD", { encoding: "utf-8", timeout: 5000 }).trim();
} catch {
  // Not a git repo or git unavailable — keep default.
}

const content = `// Generated by scripts/gen-build-info.mjs — do not edit manually.\nexport const BUILD_COMMIT = ${JSON.stringify(commit)} as const;\n`;
writeFileSync(outFile, content, "utf-8");
console.log(`[gen-build-info] BUILD_COMMIT = ${commit.slice(0, 8)}`);
